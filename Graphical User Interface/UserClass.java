package application;

public class UserClass {
	
	public String Username;
	public String Password;
	
	
	public UserClass(String name, String PWord) {
		Username = name;
		Password = PWord;
		
	}
	
	public String getuser() {
		return Username;
	}
	public String getpassword() {
		return Password;
	}
	
	
	
	



}

